#define to(T, x) ((T)(x))

#define to_char(x) ((char)(x))
#define to_short(x) ((short)(x))
#define to_int(x) ((int)(x))
#define to_long(x) ((long)(x))
#define to_longlong(x) ((long long)(x))

#define to_schar(x) ((signed char)(x))

#define to_uchar(x) ((unsigned char)(x))
#define to_ushort(x) ((unsigned short)(x))
#define to_uint(x) ((unsigned int)(x))
#define to_ulong(x) ((unsigned long)(x))
#define to_ulonglong(x) ((unsigned long long)(x))

#define to_int8(x) ((int8_t)(x))
#define to_int16(x) ((int16_t)(x))
#define to_int32(x) ((int32_t)(x))
#define to_int64(x) ((int64_t)(x))

#define to_uint8(x) ((uint8_t)(x))
#define to_uint16(x) ((uint16_t)(x))
#define to_uint32(x) ((uint32_t)(x))
#define to_uint64(x) ((uint64_t)(x))
